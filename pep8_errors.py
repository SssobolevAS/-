def get_error_info(error_code: str) -> dict:
    """
    Возвращает информацию об ошибке по её коду.
    
    Args:
        error_code (str): Код ошибки PEP 8
        
    Returns:
        dict: Информация об ошибке
    """
    # Нормализуем код ошибки
    normalized_code = error_code.upper()
    
    # Ищем точное совпадение
    if normalized_code in PEP8_ERRORS:
        return PEP8_ERRORS[normalized_code]
    
    # Ищем частичное совпадение (например, E50 для всех E50x)
    prefix = normalized_code[:3] if len(normalized_code) >= 3 else normalized_code
    for code in PEP8_ERRORS:
        if code.startswith(prefix):
            return PEP8_ERRORS[code]
    
    # Возвращаем информацию для неизвестной ошибки
    return PEP8_ERRORS["UNKNOWN"]